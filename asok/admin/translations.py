def translate(locale, key, **kwargs):
    """Look up `key` in `locale`; fall back to English (= key)."""
    if not locale or locale == "en":
        res = key
    else:
        cat = MESSAGES.get(locale)
        if not cat:
            res = key
        else:
            res = cat.get(key, key)

    if kwargs:
        try:
            return res.format(**kwargs)
        except Exception:
            return res
    return res
